# 払戻情報テーブルのカラム名リスト（222カラム）
def _generate_payoff_columns() -> list[str]:
    """払戻情報テーブルのカラム名リストを生成する."""
    columns: list[str] = [
        "レースコード",
        "開催年",
        "開催月日",
        "競馬場",
        "開催回",
        "開催日目",
        "レース番号",
        "登録頭数",
        "出走頭数",
    ]

    # 不成立フラグ
    for kenshu in ["単勝", "複勝", "枠連", "馬連", "ワイド", "馬単", "3連複", "3連単"]:
        columns.append(f"不成立フラグ{kenshu}")

    # 特払フラグ
    for kenshu in ["単勝", "複勝", "枠連", "馬連", "ワイド", "馬単", "3連複", "3連単"]:
        columns.append(f"特払フラグ{kenshu}")

    # 返還フラグ
    for kenshu in ["単勝", "複勝", "枠連", "馬連", "ワイド", "馬単", "3連複", "3連単"]:
        columns.append(f"返還フラグ{kenshu}")

    # 返還馬番情報1〜28
    for i in range(1, 29):
        columns.append(f"返還馬番情報{i}")

    # 返還枠番情報1〜8
    for i in range(1, 9):
        columns.append(f"返還枠番情報{i}")

    # 返還同枠情報1〜8
    for i in range(1, 9):
        columns.append(f"返還同枠情報{i}")

    # 単勝1〜3
    for i in range(1, 4):
        columns.extend([f"単勝{i}馬番", f"単勝{i}払戻金", f"単勝{i}人気順"])

    # 複勝1〜5
    for i in range(1, 6):
        columns.extend([f"複勝{i}馬番", f"複勝{i}払戻金", f"複勝{i}人気順"])

    # 枠連1〜3
    for i in range(1, 4):
        columns.extend([f"枠連{i}組番1", f"枠連{i}組番2", f"枠連{i}払戻金", f"枠連{i}人気順"])

    # 馬連1〜3
    for i in range(1, 4):
        columns.extend([f"馬連{i}組番1", f"馬連{i}組番2", f"馬連{i}払戻金", f"馬連{i}人気順"])

    # ワイド1〜7
    for i in range(1, 8):
        columns.extend(
            [f"ワイド{i}組番1", f"ワイド{i}組番2", f"ワイド{i}払戻金", f"ワイド{i}人気順"]
        )

    # 馬単1〜6
    for i in range(1, 7):
        columns.extend([f"馬単{i}組番1", f"馬単{i}組番2", f"馬単{i}払戻金", f"馬単{i}人気順"])

    # 3連複1〜3
    for i in range(1, 4):
        columns.extend(
            [
                f"3連複{i}組番1",
                f"3連複{i}組番2",
                f"3連複{i}組番3",
                f"3連複{i}払戻金",
                f"3連複{i}人気順",
            ]
        )

    # 3連単1〜6
    for i in range(1, 7):
        columns.extend(
            [
                f"3連単{i}組番1",
                f"3連単{i}組番2",
                f"3連単{i}組番3",
                f"3連単{i}払戻金",
                f"3連単{i}人気順",
            ]
        )

    return columns


